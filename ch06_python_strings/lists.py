# List Items
my_list = ["apple", "banana", "orange", "peach"]
print(my_list)
# List items are ordered, changeable and indexed, index is the position of item in list, e.g appli is at index [0] and so on...
# Accessing list items
print(my_list[0])
print(my_list[1])
print(my_list[2])
print(my_list[-1])
# Length of list
print(len(my_list))
